//Bài 1: In ra các số chẵn từ 1 -> 100 dùng for và while
//Dùng for
for(let i = 1; i <= 100; i++) {
    if(i%2 == 0) {
        console.log(i)
    }
}

// Dùng while
let i2 = 1
while(i2 <= 100) {
    if( i2%2 == 0){
    console.log(i2)
    }
    i2++;
}

